% Построить поверхность, заданную неявным уравнением
%-x^2*z^3 - 9*y^2*z^3/80 + (x^2 + 9*y^2/4 + z^2 - 1)^3 = 0
f = @(x,y,z) -(x.^2).*(z.^3)-9*(y.^2).*((z.^3)/80)...
    +(x.^2+9*(y.^2)/4+(z.^2)-1).^3;
interval = [-3 3 -3 3 -3 3];
fimplicit3(f, interval,'MeshDensity',100,'EdgeColor','None');
colormap('hot');
title('любовь к математике');
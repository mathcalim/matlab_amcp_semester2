%Построить график функции f(r) = {2J_1(ar)/r}^2, где a = pi, J_1 = функция
%Бесселя первого порядка и r = sqrt(x^2+y^2). диапазон изменения координат:
%от -1 до 1.
clc
a = 2*pi;
[x,y] = meshgrid(-1:0.01:1,-1:0.01:1);
R = (x.^2+y.^2).^(1/2);
z = (2*besselj(1,a*R(:))./R(:)).^2;
mesh(x,y,reshape(z,size(x)));
set(gca,'Zscale','Log');